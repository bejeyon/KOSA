package classes;
class MultiVariable{
	public static void main(String[] args) 	{
		int x1 = 5;
		int y1 = 6;
		int z1 = 50;
		System.out.println(x1 + y1 + z1);
		int x2 = 5, y2 = 6, z2 = 50;
		System.out.println(x2 + y2 + z2);
		int x3, y3, z3;
		x3 = y3 = z3 = 50;
		System.out.println(x3 + y3 + z3);
	}
}