package classes;

class CharChk01{

	public static void main(String[] args) {
		char vC01 = 'A'; 	// 정수값 저장됨
		char vC02 = 'a';
		char vC03 = '0';
		
		System.out.println(vC01);
		System.out.println((int)vC01);
		System.out.println(vC02);
		System.out.println((int)vC02);
		System.out.println(vC03);
		System.out.println((int)vC03);
		System.out.println("\n\n\n\n");
	}
}
