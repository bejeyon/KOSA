package classes;

class Chap03_ExerciseQ6
{
	public static void main(String[] args) 
	{
		System.out.println(false && true);			// false
		System.out.println((4 <= 4) || (6 < 3));	// true
		System.out.println(false ^ (3 >= 4));		// false
		System.out.println(!(3 <= 3));					// false
	}
}